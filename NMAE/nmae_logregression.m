x = [1900 1910 1920 1930 1940 1950 1960 1970 1980 1990 2000 2010];
y = [47.3 50 54.1 59.7 62.9 68.2 69.7 70.8 73.7 75.4 76.8 78.7];
n = length(x);
sum_ln_x = sum(log(x));
sum_y = sum(y);
sum_ln_x2 = sum(log(x).^2);
sum_ln_x_y = sum(log(x).*y);
A=[n sum_ln_x; sum_ln_x sum_ln_x2];
B = [sum_y;sum_ln_x_y];
C=A\B;
a=C(1,1);
b=C(2,1);
fprintf('a0 = ');
disp(a);
fprintf('a1 = ');
disp(b);
f=@(x) a + b * log(x);
scatter(x, y, 'filled');
hold on;
fplot(f,[1880,2050], 'r');
xlabel('X');
ylabel('Y');
title('Logarithmic Regression (Manual)');
legend('Data', 'Logarithmic Fit', 'Location', 'best');
grid on;
hold off;
